class OperacoesMatematicas:
    def soma(self, primeiro_numero, segundo_numero):
        return primeiro_numero + segundo_numero

    def subtracao(self, primeiro_numero, segundo_numero):
        return primeiro_numero - segundo_numero

    def multiplicacao(self, primeiro_numero, segundo_numero):
        return primeiro_numero * segundo_numero

    def divisao(self, primeiro_numero, segundo_numero):
        return primeiro_numero / segundo_numero
